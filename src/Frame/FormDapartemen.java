    public void tampilDataDapartemen(){
         try {
            modelTabelDapartemen.setRowCount(0);

            String query = "SELECT * FROM tb_dapartemen";
            try (PreparedStatement preparedStatement = obj.konek.prepareStatement(query);
                 ResultSet data = preparedStatement.executeQuery()) {
                while (data.next()) {
                    Object[] line = new Object[2];
                    line[0] = data.getString("id_dapartemen");
                    line[1] = data.getString("nama_dapartemen");
                    modelTabelDapartemen.addRow(line);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

