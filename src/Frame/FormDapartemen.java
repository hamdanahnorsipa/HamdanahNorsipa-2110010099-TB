    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
         try {
           if (tfIdDapartemen.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "ID Dapartemen belum Diisi");
               tfIdDapartemen.requestFocus();
           } else if (tfNamaDapartemen.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "Nama Dapartemen belum Diisi");
               tfNamaDapartemen.requestFocus();
           } else {
               String sql = "insert into tb_dapartemen value('"+tfIdDapartemen.getText()+"','"+tfNamaDapartemen.getText()+"')";
               obj.konek.createStatement().execute(sql);
               JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
           }
           tampilDataDapartemen();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

