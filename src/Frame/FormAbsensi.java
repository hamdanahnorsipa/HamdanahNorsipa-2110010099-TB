    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
      try {
        if (tfIdAbsensi.getText().isEmpty() || tfIdPegawai.getText().isEmpty() ||
            tfTanggal.getText().isEmpty() || tfJamMasuk.getText().isEmpty() || tfJamKeluar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Silahkan isi semua data terlebih dahulu!");
        } else {
        // Bagian untuk INSERT
        String sqlInsert = "INSERT INTO tb_absensi (id_absensi, id_pegawai, tanggal, jam_masuk, jam_keluar) " +
                           "VALUES (?, ?, ?, ?, ?)";
        PreparedStatement insertStmt = obj.konek.prepareStatement(sqlInsert);

        insertStmt.setString(1, tfIdAbsensi.getText());
        insertStmt.setString(2, tfIdPegawai.getText());
        insertStmt.setString(3, tfTanggal.getText());
        insertStmt.setString(4, tfJamMasuk.getText());
        insertStmt.setString(5, tfJamKeluar.getText());

        // Bagian untuk UPDATE
        String sqlUpdate = "UPDATE tb_absensi SET id_pegawai=?, tanggal=?, jam_masuk=?, " +
                           "jam_keluar=? WHERE id_absensi=?";
        PreparedStatement updateStmt = obj.konek.prepareStatement(sqlUpdate);

        updateStmt.setString(1, tfIdPegawai.getText());
        updateStmt.setString(2, tfTanggal.getText());
        updateStmt.setString(3, tfJamMasuk.getText());
        updateStmt.setString(4, tfJamKeluar.getText());
        updateStmt.setString(5, tfIdAbsensi.getText());

        // Eksekusi query UPDATE
        int hasilUpdate = updateStmt.executeUpdate();

        // Jika tidak ada baris yang terpengaruh (data tidak ada), eksekusi query INSERT
        if (hasilUpdate == 0) {
            int hasilInsert = insertStmt.executeUpdate();

            if (hasilInsert > 0) {
                tampilDataAbsensi();
                JOptionPane.showMessageDialog(this, "Data Berhasil Diubah!");
            } else {
                JOptionPane.showMessageDialog(this, "Gagal mengubah data. Pastikan ID Absensi yang dimasukkan benar.");
            }
        } else {
            tampilDataAbsensi();
            JOptionPane.showMessageDialog(this, "Data Berhasil Diubah!");
        }
    }
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
}
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
      try {
        // Mengecek apakah kolom input ID Absensi, ID Pegawai, Tanggal, Jam Masuk, dan Jam Keluar kosong
        if (tfIdAbsensi.getText().isEmpty() && tfIdPegawai.getText().isEmpty()
                && tfTanggal.getText().isEmpty() && tfJamMasuk.getText().isEmpty()
                && tfJamKeluar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Silahkan pilih data terlebih dahulu!");
        } else {
            // Menampilkan dialog konfirmasi sebelum menghapus
            int confirmDialogResult = JOptionPane.showConfirmDialog(this,
                    "Apakah Anda yakin ingin menghapus data?", "Konfirmasi Hapus",
                    JOptionPane.YES_NO_OPTION);

            // Jika pengguna mengonfirmasi untuk menghapus
            if (confirmDialogResult == JOptionPane.YES_OPTION) {
                // Query SQL untuk menghapus data dari tabel absensi
                String sql = "DELETE FROM tb_absensi WHERE id_absensi=?";
                
                // Menggunakan try-with-resources untuk PreparedStatement
                try (PreparedStatement hapus = obj.konek.prepareStatement(sql)) {
                    hapus.setString(1, tfIdAbsensi.getText());
                    
                    // Menjalankan query DELETE dan mendapatkan jumlah baris yang terpengaruh
                    int rowsAffected = hapus.executeUpdate();

                    // Jika ada baris yang terpengaruh, berarti data berhasil dihapus
                    if (rowsAffected > 0) {
                        tampilDataAbsensi();
                        JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus!");
                    } else {
                        // Jika tidak ada baris yang terpengaruh, berarti data tidak ditemukan
                        JOptionPane.showMessageDialog(this, "Gagal menghapus data. Data tidak ditemukan.");
                    }
                }
            }
        }
    } catch (SQLException | HeadlessException e) {
        // Menangani kesalahan dan menampilkan pesan kesalahan kepada pengguna
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnHapusActionPerformed

