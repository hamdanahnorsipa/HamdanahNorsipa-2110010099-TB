    public void tampilDataAbsensi() {
         try {
            modelTabelAbsensi.setRowCount(0);

            String query = "SELECT * FROM tb_absensi";
            try (PreparedStatement preparedStatement = obj.konek.prepareStatement(query);
                 ResultSet data = preparedStatement.executeQuery()) {
                while (data.next()) {
                    Object[] line = new Object[5];
                    line[0] = data.getString("id_absensi");
                    line[1] = data.getString("id_pegawai");
                    line[2] = data.getString("tanggal");
                    line[3] = data.getString("jam_masuk");
                    line[4] = data.getString("jam_keluar");
                    modelTabelAbsensi.addRow(line);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

