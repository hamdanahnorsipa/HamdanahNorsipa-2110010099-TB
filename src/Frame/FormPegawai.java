    public void tampilDataPegawai() {
        try {
            modelTabelPegawai.setRowCount(0);

            String query = "SELECT * FROM tb_pegawai";
            try (PreparedStatement preparedStatement = obj.konek.prepareStatement(query);
                 ResultSet data = preparedStatement.executeQuery()) {
                while (data.next()) {
                    Object[] line = new Object[9];
                    line[0] = data.getString("id_pegawai");
                    line[1] = data.getString("id_dapartemen");
                    line[2] = data.getString("nama");
                    line[3] = data.getString("tanggal_lahir");
                    line[4] = data.getString("jenis_kelamin");
                    line[5] = data.getString("alamat");
                    line[6] = data.getString("nomor_telepon");
                    line[7] = data.getString("email");
                    line[8] = data.getString("posisi");
                    modelTabelPegawai.addRow(line);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
