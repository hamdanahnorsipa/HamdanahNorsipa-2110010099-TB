    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
      try {
        if (tfIdPegawai.getText().isEmpty() || tfIdDapartemen.getText().isEmpty() ||
            tfNama.getText().isEmpty() || tfTglLahir.getText().isEmpty() ||
            tfJK.getText().isEmpty() || tfAlamat.getText().isEmpty() || 
            tfNoTelpon.getText().isEmpty() || tfEmail.getText().isEmpty() || tfPosisi.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Silahkan isi semua data terlebih dahulu!");
        } else {
        // Bagian untuk INSERT
        String sqlInsert = "INSERT INTO tb_pegawai (id_pegawai, id_dapartemen, nama, tanggal_lahir, jenis_kelamin, alamat, nomor_telepon, email, posisi) " +
                           "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
        PreparedStatement insertStmt = obj.konek.prepareStatement(sqlInsert);

        insertStmt.setString(1, tfIdPegawai.getText());
        insertStmt.setString(2, tfIdDapartemen.getText());
        insertStmt.setString(3, tfNama.getText());
        insertStmt.setString(4, tfTglLahir.getText());
        insertStmt.setString(5, tfJK.getText());
        insertStmt.setString(6, tfAlamat.getText());
        insertStmt.setString(7, tfNoTelpon.getText());
        insertStmt.setString(8, tfEmail.getText());
        insertStmt.setString(9, tfPosisi.getText());

        // Bagian untuk UPDATE
        String sqlUpdate = "UPDATE tb_pegawai SET id_dapartemen=?, nama=?, " +
                           "tanggal_lahir=?, jenis_kelamin=?, alamat=?, nomor_telepon=?, " +
                           "email=?, posisi=? WHERE id_pegawai=?";
        PreparedStatement updateStmt = obj.konek.prepareStatement(sqlUpdate);

        updateStmt.setString(1, tfIdDapartemen.getText());
        updateStmt.setString(2, tfNama.getText());
        updateStmt.setString(3, tfTglLahir.getText());
        updateStmt.setString(4, tfJK.getText());
        updateStmt.setString(5, tfAlamat.getText());
        updateStmt.setString(6, tfNoTelpon.getText());
        updateStmt.setString(7, tfEmail.getText());
        updateStmt.setString(8, tfPosisi.getText());
        updateStmt.setString(9, tfIdPegawai.getText());

        // Eksekusi query UPDATE
        int hasilUpdate = updateStmt.executeUpdate();

        // Jika tidak ada baris yang terpengaruh (data tidak ada), eksekusi query INSERT
        if (hasilUpdate == 0) {
            int hasilInsert = insertStmt.executeUpdate();

            if (hasilInsert > 0) {
                tampilDataPegawai();
                JOptionPane.showMessageDialog(this, "Data berhasil diubah!");
            } else {
                JOptionPane.showMessageDialog(this, "Gagal mengubah data. Pastikan ID Pegawai yang dimasukkan benar.");
            }
        } else {
            tampilDataPegawai();
            JOptionPane.showMessageDialog(this, "Data Berhasil diubah!");
        }
    }
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
}
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
     try {
        // Mengecek apakah semua kolom input kosong
        if (tfIdPegawai.getText().isEmpty() && tfIdDapartemen.getText().isEmpty()
                && tfNama.getText().isEmpty() && tfTglLahir.getText().isEmpty()
                && tfJK.getText().isEmpty() && tfAlamat.getText().isEmpty()
                && tfNoTelpon.getText().isEmpty() && tfEmail.getText().isEmpty()
                && tfPosisi.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Silahkan pilih data terlebih dahulu!");
        } else {
            // Menampilkan dialog konfirmasi sebelum menghapus
            int confirmDialogResult = JOptionPane.showConfirmDialog(this,
                    "Apakah Anda yakin ingin menghapus data?", "Konfirmasi Hapus",
                    JOptionPane.YES_NO_OPTION);

            // Jika pengguna mengonfirmasi untuk menghapus
            if (confirmDialogResult == JOptionPane.YES_OPTION) {
                // Query SQL untuk menghapus data dari tabel
                String sql = "DELETE FROM tb_pegawai WHERE id_pegawai=?";
                
                // Menggunakan try-with-resources untuk PreparedStatement
                try (PreparedStatement hapus = obj.konek.prepareStatement(sql)) {
                    hapus.setString(1, tfIdPegawai.getText());
                    
                    // Menjalankan query DELETE dan mendapatkan jumlah baris yang terpengaruh
                    int rowsAffected = hapus.executeUpdate();

                    // Jika ada baris yang terpengaruh, berarti data berhasil dihapus
                    if (rowsAffected > 0) {
                        tampilDataPegawai();
                        JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus!");
                    } else {
                        // Jika tidak ada baris yang terpengaruh, berarti data tidak ditemukan
                        JOptionPane.showMessageDialog(this, "Gagal menghapus data. Data tidak ditemukan.");
                    }
                }
            }
        }
    } catch (SQLException | HeadlessException e) {
        // Menangani kesalahan dan menampilkan pesan kesalahan kepada pengguna
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnHapusActionPerformed

